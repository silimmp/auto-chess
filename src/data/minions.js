let nextInstanceId = 1;

function cloneMinionAbilityFields(minion) {
  return Object.fromEntries(
    MINION_ABILITY_FIELDS.map((field) => [field, minion[field] ? { ...minion[field] } : null])
  );
}

function createMinionSnapshot(minion, runtimeFields = {}) {
  return {
    cardKind: "minion",
    id: minion.id,
    name: minion.name,
    tier: minion.tier,
    tribe: minion.tribe,
    attack: minion.attack,
    health: minion.health,
    keywords: [...minion.keywords],
    ...cloneMinionAbilityFields(minion),
    text: minion.text,
    token: Boolean(minion.token),
    golden: Boolean(runtimeFields.golden ?? minion.golden),
    instanceId: runtimeFields.instanceId ?? minion.instanceId ?? null,
  };
}

function cloneTemplate(minion) {
  return createMinionSnapshot(minion);
}

function copyMinion(minion) {
  return createMinionSnapshot(minion, {
    golden: Boolean(minion.golden),
    instanceId: minion.instanceId ?? null,
  });
}

function cloneForBattle(minion) {
  return copyMinion(minion);
}

function createOwnedMinion(id) {
  const template = byId.get(id);
  return {
    ...cloneTemplate(template),
    instanceId: nextInstanceId++,
    golden: false,
  };
}

function createGoldenMinion(base) {
  return {
    ...copyMinion(base),
    attack: base.attack * 2,
    health: base.health * 2,
    golden: true,
    instanceId: nextInstanceId++,
  };
}

function createBrandSnapshot(brand, runtimeFields = {}) {
  return {
    cardKind: "brandSpell",
    id: brand.id,
    name: brand.name,
    tribe: brand.tribe,
    tier: 0,
    text: brand.text,
    meta: brand.meta || "物品牌",
    effect: {
      type: brand.effect.type,
      attack: brand.effect.attack ?? 0,
      health: brand.effect.health ?? 0,
      addKeywords: [...(brand.effect.addKeywords || [])],
    },
    instanceId: runtimeFields.instanceId ?? brand.instanceId ?? null,
  };
}


const byId = new Map(MINION_POOL.map((minion) => [minion.id, minion]));

let nextInstanceId = 1;

function cloneMinionAbilityFields(minion) {
  return Object.fromEntries(
    MINION_ABILITY_FIELDS.map((field) => [field, minion[field] ? { ...minion[field] } : null])
  );
}

export function createMinionSnapshot(minion, runtimeFields = {}) {
  return {
    id: minion.id,
    name: minion.name,
    tier: minion.tier,
    tribe: minion.tribe,
    attack: minion.attack,
    health: minion.health,
    keywords: [...minion.keywords],
    ...cloneMinionAbilityFields(minion),
    text: minion.text,
    token: Boolean(minion.token),
    golden: Boolean(runtimeFields.golden ?? minion.golden),
    instanceId: runtimeFields.instanceId ?? minion.instanceId ?? null,
  };
}

export function cloneTemplate(minion) {
  return createMinionSnapshot(minion);
}

export function copyMinion(minion) {
  return createMinionSnapshot(minion, {
    golden: Boolean(minion.golden),
    instanceId: minion.instanceId ?? null,
  });
}

export function cloneForBattle(minion) {
  return copyMinion(minion);
}

export function createOwnedMinion(id) {
  const template = byId.get(id);
  return {
    ...cloneTemplate(template),
    instanceId: nextInstanceId++,
    golden: false,
  };
}

export function createGoldenMinion(base) {
  return {
    ...copyMinion(base),
    attack: base.attack * 2,
    health: base.health * 2,
    golden: true,
    instanceId: nextInstanceId++,
  };
}

export { MINION_POOL, byId };
